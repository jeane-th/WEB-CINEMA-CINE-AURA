// 🔹 Generar fechas: hoy y los próximos 2 días
function generarFechas(dias = 3) {
  const fechas = [];
  const hoy = new Date();
  for (let i = 0; i < dias; i++) {
    const f = new Date(hoy);
    f.setDate(hoy.getDate() + i);
    const fechaFormateada = f.toISOString().split("T")[0];
    fechas.push(fechaFormateada);
  }
  return fechas;
}

// 🔹 Generar cartelera completa
function generarFunciones() {
  const salas = ["Sala 1", "Sala 2", "Sala 3"];
  const horarios = ["10:00", "13:00", "16:00", "19:00", "22:00"];
  const fechas = generarFechas();

  const funciones = [];

  fechas.forEach((fecha) => {
    salas.forEach((sala) => {
      horarios.forEach((hora) => {
        const pelicula = dataPelis[Math.floor(Math.random() * dataPelis.length)];
        funciones.push({
          fecha,
          sala,
          hora,
          pelicula: pelicula.titulo,
        });
      });
    });
  });

  return funciones;
}

